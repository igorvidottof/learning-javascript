// Other way to convert a string to a number
// This function Number "understand" when a string is a Integer or a Decimal

var numAsString = '78';
console.log(Number(numAsString) + 20);

numAsString = '89.27';
console.log(Number(numAsString) + 20);


// Converting Numbers to Strings
var num = 872;
console.log(num.toString() + 20);

